interface Layer {
  id: string;
  layout?: any;
  paint?: any;
  type?: any;
}

const mergeLayers = (layers: Layer[], overrides: Layer[]) => {
  const overridesById = keyBy(overrides, (x) => x.id);
  return layers.map((layer) => {
    return merge({}, layer, overridesById[layer.id]);
  });
};

